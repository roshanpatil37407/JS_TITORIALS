const num = 10;
if (num > 0) {
    console.log('The number is positive');
} else if (num < 0) {
    console.log('The number is negative');
} else {
    console.log('The number is zero');
}

// Switch Case
const day = 3;
switch (day) {
    case 1:
        console.log('Monday');
        break;
    case 2:
        console.log('Tuesday');
        break;
    case 3:
        console.log('Wednesday');
        break;
    case 4:
        console.log('Thursday');
        break;
    case 5:
        console.log('Friday');
        break;
    case 6:
        console.log('Saturday');
        break;
    case 7:
        console.log('Sunday');
        break;
    default:
        console.log('Invalid day');
}
