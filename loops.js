// For Loop
for (let i = 0; i < 5; i++) {
    console.log(`For Loop: ${i}`);
}

// While Loop
let count = 0;
while (count < 5) {
    console.log(`While Loop: ${count}`);
    count++;
}

// Do-While Loop
let index = 0;
do {
    console.log(`Do-While Loop: ${index}`);
    index++;
} while (index < 5);
